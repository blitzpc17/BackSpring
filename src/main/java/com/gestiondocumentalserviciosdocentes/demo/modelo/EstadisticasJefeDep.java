package com.gestiondocumentalserviciosdocentes.demo.modelo;

public class EstadisticasJefeDep {

	public String nombreDocente;
	public String reporteFinal;
	public String numDocumentos;
	public String liberacion;
	public String getNombreDocente() {
		return nombreDocente;
	}
	public void setNombreDocente(String nombreDocente) {
		this.nombreDocente = nombreDocente;
	}
	public String getReporteFinal() {
		return reporteFinal;
	}
	public void setReporteFinal(String reporteFinal) {
		this.reporteFinal = reporteFinal;
	}
	public String getNumDocumentos() {
		return numDocumentos;
	}
	public void setNumDocumentos(String numDocumentos) {
		this.numDocumentos = numDocumentos;
	}
	public String getLiberacion() {
		return liberacion;
	}
	public void setLiberacion(String liberacion) {
		this.liberacion = liberacion;
	}
}
